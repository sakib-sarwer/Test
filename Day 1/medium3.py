# Calculate the average of 3 numbers

# Solution

number1 = float(input("Please enter the first number: "))
number2 = float(input("Pleae enter the second number: "))
number3 = float(input("Pleae enter the third number: "))

average = ((number1+number2+number3)/3)

print(f"The average of 3 numbers is:  {average}")